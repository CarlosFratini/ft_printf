#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	d = (char *)dst;
	s = (const char *)src;
	if (d == NULL && s == NULL)
		return (NULL);
	while (len-- > 0)
	{
		*d++ = *s++;
	}
	return (dst);
}
